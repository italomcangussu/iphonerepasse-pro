/**
 * ESC/POS byte-level builder for 80mm thermal printers (Epson TM, Elgin, Bematech).
 * Text is encoded in CP850 (PC Multilingual Latin-I) for full Brazilian Portuguese support.
 */

const LF = 0x0A;

// Unicode code point → CP850 byte (only extended chars; ASCII 0x00-0x7F is identity)
const CP850_MAP: Record<number, number> = {
  0x00C7: 0x80, 0x00FC: 0x81, 0x00E9: 0x82, 0x00E2: 0x83, 0x00E4: 0x84,
  0x00E0: 0x85, 0x00E5: 0x86, 0x00E7: 0x87, 0x00EA: 0x88, 0x00EB: 0x89,
  0x00E8: 0x8A, 0x00EF: 0x8B, 0x00EE: 0x8C, 0x00EC: 0x8D, 0x00C4: 0x8E,
  0x00C5: 0x8F, 0x00C9: 0x90, 0x00E6: 0x91, 0x00C6: 0x92, 0x00F4: 0x93,
  0x00F6: 0x94, 0x00F2: 0x95, 0x00FB: 0x96, 0x00F9: 0x97, 0x00FF: 0x98,
  0x00D6: 0x99, 0x00DC: 0x9A, 0x00F8: 0x9B, 0x00A3: 0x9C, 0x00D8: 0x9D,
  0x00E1: 0xA0, 0x00ED: 0xA1, 0x00F3: 0xA2, 0x00FA: 0xA3, 0x00F1: 0xA4,
  0x00D1: 0xA5, 0x00AA: 0xA6, 0x00BA: 0xA7, 0x00BF: 0xA8, 0x00AE: 0xA9,
  0x00C1: 0xB5, 0x00C2: 0xB6, 0x00C0: 0xB7, 0x00A9: 0xB8,
  0x00E3: 0xC6, 0x00C3: 0xC7,
  0x00CA: 0xD2, 0x00CB: 0xD3, 0x00C8: 0xD4, 0x00CD: 0xD6, 0x00CE: 0xD7,
  0x00CF: 0xD8, 0x00CC: 0xDE,
  0x00D3: 0xE0, 0x00DF: 0xE1, 0x00D4: 0xE2, 0x00D2: 0xE3, 0x00F5: 0xE4,
  0x00D5: 0xE5, 0x00DA: 0xE9, 0x00DB: 0xEA, 0x00D9: 0xEB, 0x00FD: 0xEC,
  0x00DD: 0xED, 0x00B0: 0xF8, 0x00F7: 0xF6, 0x00B1: 0xF1, 0x00B2: 0xFD,
  0x00B3: 0xFC, 0x00B9: 0xFB, 0x25A0: 0xFE, 0x00A0: 0xFF,
};

/** Characters per line on 80mm paper at Font A (12-dot pitch, 203 DPI). */
export const CHARS_PER_LINE = 48;

export class EscPosBuilder {
  private buf: number[] = [];

  raw(...bytes: number[]): this {
    this.buf.push(...bytes);
    return this;
  }

  text(s: string): this {
    for (const char of s) {
      const cp = char.codePointAt(0) ?? 0x3F;
      this.buf.push(cp < 0x80 ? cp : (CP850_MAP[cp] ?? 0x3F));
    }
    return this;
  }

  lf(): this { return this.raw(LF); }
  line(s = ''): this { return this.text(s).lf(); }

  init(): this { return this.raw(0x1B, 0x40); }
  /** Set CP850 (PC Multilingual Latin-I) code page — covers full Brazilian Portuguese. */
  cp850(): this { return this.raw(0x1B, 0x74, 0x02); }

  left(): this   { return this.raw(0x1B, 0x61, 0x00); }
  center(): this { return this.raw(0x1B, 0x61, 0x01); }
  right(): this  { return this.raw(0x1B, 0x61, 0x02); }

  bold(on: boolean): this { return this.raw(0x1B, 0x45, on ? 1 : 0); }

  /** w, h: 1 = normal, 2 = double. */
  size(w: 1 | 2, h: 1 | 2): this {
    return this.raw(0x1D, 0x21, ((w - 1) << 4) | (h - 1));
  }

  /** Feed n blank lines. */
  feed(n: number): this { return this.raw(0x1B, 0x64, n); }

  /** Partial cut with 6-dot feed (standard for most printers). */
  cut(): this { return this.raw(0x1D, 0x56, 0x42, 0x06); }

  /** Full separator line. */
  sep(char = '-', width = CHARS_PER_LINE): this {
    return this.line(char.repeat(width));
  }

  /**
   * Two-column row padded to `width` chars.
   * Left text is truncated to fit; right text anchors to the right edge.
   */
  row(left: string, right: string, width = CHARS_PER_LINE): this {
    const r = right.slice(0, width - 1);
    const maxLeft = width - r.length;
    const l = left.slice(0, maxLeft).padEnd(maxLeft);
    return this.line(l + r);
  }

  build(): Uint8Array {
    return new Uint8Array(this.buf);
  }
}
